    Ca_group next_ca_group(const Ca_group &ca_group, const clipper::Xmap<float> &xmap)
    {
        clipper::RTop_orth rtop = ca_group.rtop_from_std_ori();
        float input_array[648];
        float sin_array[3];
        float cos_array[3];
        for (int i = 0; i < 648; i++)
        {
            auto pos = _NEXT_PROBES[i].transform(rtop).coord_frac(xmap.cell());
            auto value = xmap.interp<clipper::Interp_cubic>(pos);
            input_array[i] = value;
        }
        k2c_tensor input_tensor = {&input_array[0], 1, 648, {648, 1, 1, 1, 1}};
        k2c_tensor sin_tensor = {&sin_array[0], 1, 3, {3, 1, 1, 1, 1}};
        k2c_tensor cos_tensor = {&cos_array[0], 1, 3, {3, 1, 1, 1, 1}};
        _zscore(input_tensor);
        ml_grow_next(&input_tensor, &sin_tensor, &cos_tensor);
        float psi = atan2(sin_tensor.array[0], cos_tensor.array[0]);
        float omega = atan2(sin_tensor.array[1], cos_tensor.array[1]);
        float phi = atan2(sin_tensor.array[2], cos_tensor.array[2]);
        return ca_group.next_ca_group(psi, omega, phi);
    }

