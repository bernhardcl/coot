    void _zscore(k2c_tensor &tensor)
    {
        float count = 0.0;
        float sum = 0.0;
        float sum2 = 0.0;
        for (int i = 0; i < tensor.numel; i++)
        {
            const float value = tensor.array[i];
            count += 1.0;
            sum += value;
            sum2 += value * value;
        }
        const float mean = sum / count;
        const float mean2 = sum2 / count;
        const float std = sqrt(mean2 - mean * mean);
        for (int i = 0; i < tensor.numel; i++)
            tensor.array[i] = (tensor.array[i] - mean) / std;
    }

